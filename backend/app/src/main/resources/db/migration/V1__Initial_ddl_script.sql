  process_end_date TIMESTAMP(6),
  status_code VARCHAR(30),
  created_by varchar(45) DEFAULT CURRENT_USER,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_by VARCHAR(45),	
